import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = path.join(root, "backend/src/main/resources/db/migration/V1__schema.sql");
const outputDir = path.join(root, "backend/src/main/resources/db/dev");
const outputPath = path.join(outputDir, "schema-h2.sql");

const source = await readFile(sourcePath, "utf8");
const transformed = source
  .replace(/\s+UNSIGNED\b/g, "")
  .replace(/\bENUM\([^)]*\)/g, "VARCHAR(32)")
  // H2's native JSON type re-encodes a bound JSON *string* as a quoted JSON
  // scalar, so `shipping_address` reads back double-encoded and Jackson fails
  // to deserialize the order. Store it as TEXT locally; production MySQL keeps
  // the JSON column (it parses a bound JSON string transparently).
  .replace(/\bJSON\b/g, "TEXT")
  .replace(/\s+ON UPDATE CURRENT_TIMESTAMP\(6\)/g, "")
  .replace(/^\s*(?:FULLTEXT\s+)?INDEX\s+[^\r\n]+,?\r?\n/gm, "")
  .replace(/^\s*UNIQUE KEY\s+(\w+)\s+\(([^)]+)\),?$/gm, "    CONSTRAINT $1 UNIQUE ($2),")
  .replace(/,\r?\n\)/g, "\n)")
  .replace(/\) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;/g, ");");

const header = [
  "-- Generated by scripts/generate-backend-dev-schema.mjs. Do not edit manually.",
  "-- H2-only local integration schema. Production continues to use Flyway db/migration/V1__schema.sql.",
  "",
].join("\n");

await mkdir(outputDir, { recursive: true });
await writeFile(outputPath, header + transformed, "utf8");
console.log(`Generated ${path.relative(root, outputPath)}`);
