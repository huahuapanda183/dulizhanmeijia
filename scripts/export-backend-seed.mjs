const collectionPositions = new Map();
for (const [featuredPosition, product] of PRODUCTS.entries()) {
  add(
    "INSERT INTO products " +
      "(id, handle, title, shape, price_cents, compare_at_price_cents, currency, rating, review_count, hover_image, video, badge, description, available, featured_position, created_at) VALUES " +
      `(${q(product.id)}, ${q(product.handle)}, ${q(product.title)}, ${q(product.shape)}, ${cents(product.price)}, ${cents(product.compareAtPrice)}, ${q(product.currency)}, ${q(product.rating)}, ${q(product.reviewCount)}, ${q(product.hoverImage)}, ${q(product.video)}, ${q(product.badge)}, ${q(product.description)}, ${q(product.available)}, ${featuredPosition}, ${sqlDateTime(product.createdAt)});`,
  );
  product.images.forEach((image, position) =>
    add(
      `INSERT INTO product_images (product_id, url, position) VALUES (${q(product.id)}, ${q(image)}, ${position});`,
    ),
  );
  product.tags.forEach((tag) =>
    add(`INSERT INTO product_tags (product_id, tag) VALUES (${q(product.id)}, ${q(tag)});`),
  );
  product.collections.forEach((handle) => {
    const position = collectionPositions.get(handle) ?? 0;
    collectionPositions.set(handle, position + 1);
    add(
      `INSERT INTO product_collections (product_id, collection_handle, position) VALUES (${q(product.id)}, ${q(handle)}, ${position});`,
    );
  });
}
add();

for (const review of REVIEWS) {
  add(
    "INSERT INTO reviews " +
      "(id, product_handle, author, rating, title, body, product_title, product_image, verified, created_at) VALUES " +
      `(${q(review.id)}, ${q(review.productHandle)}, ${q(review.author)}, ${q(review.rating)}, ${q(review.title)}, ${q(review.body)}, ${q(review.productTitle)}, ${q(review.productImage)}, ${q(review.verified)}, ${sqlDateTime(review.createdAt)});`,
  );
}
add();

for (const page of PAGES) {
  add(`INSERT INTO pages (slug, title, subtitle) VALUES (${q(page.slug)}, ${q(page.title)}, ${q(page.subtitle)});`);
  page.sections.forEach((section, sectionPosition) => {
    add(
      `INSERT INTO page_sections (page_slug, heading, position) VALUES (${q(page.slug)}, ${q(section.heading)}, ${sectionPosition});`,
    );
    add(`SET @page_section_id = LAST_INSERT_ID();`);
    section.body.forEach((paragraph, paragraphPosition) =>
      add(
        `INSERT INTO page_section_body (section_id, paragraph, position) VALUES (@page_section_id, ${q(paragraph)}, ${paragraphPosition});`,
      ),
    );
  });
  (page.faq ?? []).forEach((faq, position) =>
    add(
      `INSERT INTO faq_items (page_slug, question, answer, position) VALUES (${q(page.slug)}, ${q(faq.q)}, ${q(faq.a)}, ${position});`,
    ),
  );
}
add();

for (const post of BLOG_POSTS) {
  add(
    `INSERT INTO blog_posts (handle, title, excerpt, image, author, published_at) VALUES (${q(post.handle)}, ${q(post.title)}, ${q(post.excerpt)}, ${q(post.image)}, ${q(post.author)}, ${sqlDateTime(post.date)});`,
  );
  post.body.forEach((paragraph, position) =>
    add(
      `INSERT INTO blog_post_body (post_handle, paragraph, position) VALUES (${q(post.handle)}, ${q(paragraph)}, ${position});`,
    ),
  );
}
add();

