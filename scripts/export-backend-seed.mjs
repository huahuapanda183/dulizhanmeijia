const q = (value) => {
  if (value === undefined || value === null) return "NULL";
  if (typeof value === "boolean") return value ? "TRUE" : "FALSE";
  if (typeof value === "number") return String(value);
  return `'${String(value).replaceAll("\\", "\\\\").replaceAll("'", "''")}'`;
};
const cents = (value) => (value == null ? "NULL" : String(Math.round(value * 100)));

/**
 * Emit a MySQL DATETIME/TIMESTAMP literal: 'YYYY-MM-DD HH:MM:SS' in UTC.
 *
 * Do NOT emit ISO-8601 with a trailing 'Z'. H2 (used by the dev profile) parses
 * it happily, which is why it went unnoticed — but a bare 'Z' is not a datetime
 * format MySQL accepts, and under STRICT_TRANS_TABLES an unparseable datetime is
 * an error, not a warning. That divergence only surfaces the first time the
 * migrations meet a real MySQL.
 *
 * Accepts a date-only string ('2022-07-01'), a full ISO instant, or a Date.
 */
const sqlDateTime = (value) => {
  if (value == null) return "NULL";
  const raw = String(value);
  // Date-only input means midnight UTC.
  const date = new Date(/^\d{4}-\d{2}-\d{2}$/.test(raw) ? `${raw}T00:00:00Z` : raw);
  if (Number.isNaN(date.getTime())) throw new Error(`Unparseable date in seed data: ${raw}`);
  return `'${date.toISOString().slice(0, 19).replace("T", " ")}'`;
};
